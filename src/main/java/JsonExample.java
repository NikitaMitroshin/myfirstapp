public class JsonExample {

    private String one;
    private String key;

    public JsonExample(String one, String key) {
        this.one = one;
        this.key = key;
    }

    public JsonExample() {
    }

    public String getOne() {
        return one;
    }

    public void setOne(String one) {
        this.one = one;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}